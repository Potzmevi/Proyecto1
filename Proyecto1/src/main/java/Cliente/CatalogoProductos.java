     public void llenarTabla(JTextField filtro, String accion, boolean cliente, JTable tabla, String value, String tienda) {
        String campo = codigotxt.getText();
        String where = "";
        String ASC= "";
        if(String.valueOf(ordercombo.getSelectedItem()).equals("Ascendente")){
            ASC="ASC";
        }else{
            
            ASC="DESC";
            JOptionPane.showMessageDialog(null, ASC);
        }

        where = "WHERE " +String.valueOf(filtrocombo.getSelectedItem()) + " LIKE '%" + campo + "%'  ORDER BY "+String.valueOf(filtrocombo.getSelectedItem())+ " "+ASC;
        try {
            DefaultTableModel model = new DefaultTableModel() {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };
            tabla.setModel(model);
            String query = accion + where + tienda;
            ResultSet Result = Main.conexion.ComboBox(query);
            ResultSetMetaData rsMd = Result.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();
            model.addColumn("Codigo");
            model.addColumn("Nombre");
            model.addColumn("Fabricante");
            model.addColumn("Cantidad");
            model.addColumn("Precio");
            model.addColumn("Descripcion");
            model.addColumn("Garantia");
            model.addColumn("Tienda");
            while (Result.next()) {
                Object[] filas = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                    filas[i] = Result.getObject(i + 1);
                }
                model.addRow(filas);
            }
        } catch (SQLException e) {
            System.out.println(e.toString());
        }
    }

