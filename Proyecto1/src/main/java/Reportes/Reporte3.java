 public void CrearArchivo(JTable tabla, String titulo) {
        if (Nombretxt.getText().length() != 0) {
            try {
                PrintWriter writer = new PrintWriter("Reportes/" + Nombretxt.getText(), "UTF-8");
                writer.println("<html>");
                writer.println("<h1><center>" + titulo + "</center</h1>");
                writer.println("<style type=" + "\"" + "text/css" + "\"" + ">");
                writer.println("table, th, td {");
                writer.println("border: 1px solid black;");
                writer.println("border-collapse: collapse;");
                writer.println("}");
                writer.println(" th, td {");
                writer.println("padding: 10px;");

                writer.println("}");
                writer.println(" th {");
                writer.println("background-color: #246355;");
                writer.println("border-bottom: solid 5px #0F362D;");
                writer.println("color: white;");
                writer.println("}");
                writer.println(" tr:nth-child(even) {");
                writer.println("background-color: #ddd;");
                writer.println("}");
                writer.println(" tr:hover td{");
                writer.println("background-color: #369681;");
                writer.println("color: white;");
                writer.println("}");
                writer.println("</style>");
                writer.println("<table style=" + "\"" + "width: 100%" + "\"" + " >");
                writer.println("<tr>");
                for (int i = 0; i < tabla.getColumnCount(); i++) {
                    writer.println("<th><strong>" + tabla.getColumnName(i) + "</strong></th>");

                }
                writer.println("</tr>");
                for (int i = 0; i < tabla.getRowCount(); i++) {
                    writer.println("<tr>");
                    for (int j = 0; j < tabla.getColumnCount(); j++) {
                        writer.println("<td>" + tabla.getModel().getValueAt(i, j).toString() + "</td>");

                    }
                    writer.println("<tr>");
                }
                writer.println("</table>");
                writer.println("</html>");
                writer.close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }else {
            JOptionPane.showMessageDialog(null, "Por favor llene el nombre del archivo");
        }
    }
    public boolean compararRetraso(int dias, String fechape) {
        boolean retraso = false;
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        String fechapedido = fechape;
        LocalDate fecha1 = LocalDate.parse(fechapedido, formatter);
        LocalDate fecha2 = LocalDate.now();
        long diasdiferencia = DAYS.between(fecha1, fecha2);
        if ((int) diasdiferencia > dias) {
            retraso = true;
        } else {
            retraso = false;
        }
        return retraso;
    }

    public void llenarTabla(String accion, boolean cliente, JTable tabla, String value, String tienda) {
        String where = "";

        where = "WHERE  codigo_tienda2='" + MenuEmpresa.codigoTiendaOrigen + "'";
        try {
            DefaultTableModel model = new DefaultTableModel() {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };
            tabla.setModel(model);
            String query = "SELECT P.*,T.tiempo FROM PEDIDO P INNER JOIN TIEMPO_TIENDA T ON (P.codigo_tienda=T.codigo_tienda || P.codigo_tienda = T.codigo_tienda2)&&(P.codigo_tienda2=T.codigo_tienda || P.codigo_tienda2 = T.codigo_tienda2) LEFT JOIN RECIBO R ON P.codigo=R.codigo_pedido WHERE R.ID IS NULL && P.codigo_tienda2='" + MenuEmpresa.codigoTiendaOrigen + "'";
            ResultSet Result = Main.conexion.ComboBox(query);
            ResultSetMetaData rsMd = Result.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();
            model.addColumn("Codigo");
            model.addColumn("Fecha");
            model.addColumn("Cantidad");
            model.addColumn("Total");
            model.addColumn("Anticipo");
            model.addColumn("Producto");
            model.addColumn("Cliente");
            model.addColumn("Tienda 1");
            model.addColumn("Tienda 2");
            model.addColumn("Tiempo");
            int[] diastabla = new int[100];
            String[] fecha = new String[100];
            int contador = 0;
            while (Result.next()) {
                diastabla[contador] = Integer.parseInt(Result.getObject("tiempo").toString());
                fecha[contador] = Result.getObject("fecha").toString();

                Object[] filas = new Object[cantidadColumnas];
                if (compararRetraso(diastabla[contador], fecha[contador]) ) {

                    for (int i = 0; i < cantidadColumnas; i++) {

                        filas[i] = Result.getObject(i + 1);

                    }
                    model.addRow(filas);
                }

                contador++;
            }
        } catch (SQLException e) {
            System.out.println(e.toString());
        }
    }

    
